int main() {
    int arr[3][3];        // 声明一个 3x3 的整数二维数组
    arr[0][0] = 1;        // 给数组的第一个元素赋值
    arr[0][1] = 2;        // 给数组的第二个元素赋值
    arr[0][2] = 3;        // 给数组的第三个元素赋值
    arr[1][0] = 4;        // 给第二行的第一个元素赋值
    arr[1][1] = 5;        // 给第二行的第二个元素赋值
    arr[1][2] = 6;        // 给第二行的第三个元素赋值
    arr[2][0] = 7;        // 给第三行的第一个元素赋值
    arr[2][1] = 8;        // 给第三行的第二个元素赋值
    arr[2][2] = 9;        // 给第三行的第三个元素赋值

    // 打印二维数组的所有元素
    printf("%d %d %d\n", arr[0][0], arr[0][1], arr[0][2]);
    printf("%d %d %d\n", arr[1][0], arr[1][1], arr[1][2]);
    printf("%d %d %d\n", arr[2][0], arr[2][1], arr[2][2]);

    return 0;
}
